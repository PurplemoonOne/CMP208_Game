// @brief Functor for sorting chunks.
class CustomSort
{
public:
	bool operator()(Chunk& c1, Chunk& c2)const
	{
		return (c1.distance_to_player < c2.distance_to_player);
	}
};

//Methods for generating the level.
LevelGenerator::LevelGenerator(Context* context, b2World* world)
	:
	current_chunk_id(0),
	number_of_tiles_to_render(2)
{
	//Set all blocks to nullptr.
	for (auto& c : chunk)
	{
		//c.blocks.fill(nullptr);
	}

	//Need portal ID sounnd

	Initialise(context, world);
	SplitLevel();//Break level into chunks.
}

void LevelGenerator::Update(float delta_time)
{
	//Update each block given valid data exits.
	for (int32 index = 0; index < blocks.size(); ++index)
	{
		if (blocks.at(index) != nullptr)
		{
			//Rotate the portal.
			if (blocks.at(index)->GetObjectType() == ObjectType::environment_dynamic_)
			{
				float current_rotation = blocks.at(index)->GetRotation().y();
				blocks.at(index)->SetRotation(0.0f, gef::DegToRad((current_rotation + 12.5) * delta_time), 0.0f);
			}


			blocks.at(index)->Update(delta_time);
		}
	}
}

void LevelGenerator::UpdateChunk(float delta_time)
{
	//Update the tiles with respect to the number the player specified.
	for (int tile = 0; tile < number_of_tiles_to_render; ++tile)
	{
		for (int index = 0; index < chunk[tile].blocks.size(); ++index)
		{
			if (chunk[tile].blocks[index] != nullptr)
			{
				if(chunk[tile].blocks[index]->IsAlive())
				chunk[tile].blocks[index]->Update(delta_time);
			}
		}
	}

}

void LevelGenerator::Render(gef::Renderer3D* renderer)
{
	renderer->Begin(false);
	
		//Render the entire array of blocks.
		for (int32 index = 0; index < blocks.size(); ++index)
		{
			if (blocks.at(index) != nullptr)
			{
				if (blocks.at(index)->IsAlive())
				{
					renderer->DrawMesh(*blocks.at(index));
				}
			}
		}


	renderer->End();
}

void LevelGenerator::EvaluateChunkToRender(const gef::Vector4& player_position, bool is_moving)
{
	//Check if the player is moving.
	if (is_moving)
	{
		for (auto& c : chunk)
		{
			c.distance_to_player = abs((c.location.Length() - player_position.Length()));
		}

		//Sort the chunks based on distance to the player.
		CustomSort sort;
		std::sort(chunk.begin(), chunk.end(), sort);
	}

	current_chunk_id = chunk.front().id;
}

void LevelGenerator::RenderChunk(gef::Renderer3D* renderer)
{
	renderer->Begin(false);

	
	for (int tile = 0; tile < number_of_tiles_to_render; ++tile)
	{
		for (int index = 0; index < chunk[tile].blocks.size(); ++index)
		{
			if (chunk[tile].blocks[index] != nullptr)
			{
				if (chunk[tile].blocks[index]->IsAlive())
				renderer->DrawMesh(*chunk[tile].blocks[index]);
			}
		}
	}
	

	renderer->End();
}

