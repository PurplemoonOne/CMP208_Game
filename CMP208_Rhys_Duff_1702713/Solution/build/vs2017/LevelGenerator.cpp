//Dimensions of each tile. Hence size 32^2
#define TILE_EXTENT 32

void LevelGenerator::SplitLevel()
{
	//Chunk information.
	const int number_of_tiles = 8;
	const int number_of_horizontal_tiles = 4;
	const int number_of_vertical_tiles = 2;

	//Local index is the current block we are on with respect to the blocks container.
	int local_index = 0;

	//Offsets
	int horizontal_offset = 0;
	int horizontal_max_count = TILE_EXTENT;

	int vertical_offset = 0;
	int vertical_max_count = TILE_EXTENT;

	int current_column = 0;

	float initial_x_location = 16.0f;
	float initial_y_location = 48.0f;

	//Tracks current block index.
	int block_count = 0;

	int horizontal_iteration = 1;
	int vertical_iteration = 1;

	//Loop until we have covered every tile - I've declared 8 tiles.
	for (int global_index = 0; global_index < number_of_tiles; ++global_index)
	{
			//Loop the horizontal range - 32.
			for (int horizontal_index = 0; horizontal_index < TILE_EXTENT; ++horizontal_index)
			{
					//Loop the vertical range - 32. So we achieve nice even 32x32 tiles.
					for (int vertical_index = 0; vertical_index < TILE_EXTENT; ++vertical_index)
					{
						chunk[global_index].blocks.push_back(blocks.at(local_index));

						//Jump to the next block.
						local_index += 128;
						block_count++;
					}

					//Number of iterations computed.
					current_column += 1;

					if (global_index < 4) 
					{ 
						local_index = 0; 
					}
					else 
					{ 
						//local_index = 3968; 
						local_index = ((blocks.size() / 2) - 128);
					}

					local_index += current_column;
			}

			//Reset the block counter.
			block_count = 0;
			
			//Compute id.
			chunk[global_index].id = global_index;


			//Update the horizontal step.
			horizontal_iteration++;

			if (global_index == 3)
			{
				initial_y_location / 2.0f;
				horizontal_offset = 1;
				vertical_iteration++;

				current_column = 0;
				local_index = ((blocks.size() / 2) - 128);
			}
	}

	float x = 16.0f;
	float y = 48.0f;

	for (int index = 0; index < chunk.size(); ++index)
	{
		chunk[index].location = gef::Vector4(x, y, 10.0f);
		
		//Incriment 'x' location by 32.0f;
		x += 32.0f;
		
		//Shift 'y' down by 32.0, calculate new locations for chunks.
		if (index == 3) { x = 16.0f; y = 16.0f; }
	}

}

